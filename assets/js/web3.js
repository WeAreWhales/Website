const provider = new ethers.providers.Web3Provider(window.ethereum,"any");
const signer = provider.getSigner();
const WAW = new ethers.Contract(WAWC, ABI(), signer);


const connect = async()=>{
    await provider.send("eth_requestAccounts", []);
};

const getAddress = async()=>{
    return await signer.getAddress()
};
const mint = async() => {
    try {
			await connect();
			const amount = Number($("#quantity").val());
			const gasLimit = await WAW.estimateGas.mint(amount);
            const newGasLimit = parseInt((gasLimit * 1.2)).toString();
            await WAW.mint(amount, {gasLimit: newGasLimit})
    }
    catch (error) { 
        if (error) {
            window.alert(error);
        }
        else {
            window.alert(error);
        }
    }
};

const UpdateAccount = async () => {
    let userAddress = await getAddress();
    $("#connected").html(`Your Wallet: ${userAddress}`);
};

ethereum.on("accountsChanged", async(accounts_)=>{
    location.reload();
});

window.onload = async()=>{
    await UpdateAccount();
};
